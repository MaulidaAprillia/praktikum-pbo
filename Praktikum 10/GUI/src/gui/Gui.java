    private void jButtonSaveDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveDataActionPerformed
        // TODO add your handling code here:
        if(!items.isEmpty()){
            items.clear();
        }
        for(int i=0; i<dlm.getSize();i++){
            items.add(dlm.getElementAt(i));
        }
        updateJumDataTersimpan();
    }//GEN-LAST:event_jButtonSaveDataActionPerformed

    private void jButtonSaveToSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveToSetActionPerformed
        // TODO add your handling code here:
        if(!itemsSet.isEmpty()){
            itemsSet.clear();
        }
        for(int i=0; i<dlm.getSize();i++){
            itemsSet.add(dlm.getElementAt(i));
        }
        updateJumDataSetTersimpan();
    }//GEN-LAST:event_jButtonSaveToSetActionPerformed

    private void jButtonInsertFromSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertFromSetActionPerformed
        // TODO add your handling code here:
        for(String item : itemsSet){
            dlm.addElement(item);
        }
    }//GEN-LAST:event_jButtonInsertFromSetActionPerformed

    private void jButtonSaveToMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveToMapActionPerformed
        // TODO add your handling code here:
        if(!itemsMap.isEmpty()){
            itemsMap.clear();
        }
        for(int i=0; i<dlm.getSize();i++){
            String item = dlm.getElementAt(i);
            String key = "item_" +i;
            itemsMap.put(key,item); 
        }
        updateJumDataMapTersimpan();
    }//GEN-LAST:event_jButtonSaveToMapActionPerformed

    private void jButtonInsertFromMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertFromMapActionPerformed
        // TODO add your handling code here:
        for (Map.Entry<String,String> item : itemsMap.entrySet()){
            dlm.addElement(item.getValue());
            //System.out.println(item.getKey()+" "+item.getValue());
        }
    }//GEN-LAST:event_jButtonInsertFromMapActionPerformed

